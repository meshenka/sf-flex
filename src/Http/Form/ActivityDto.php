<?php

namespace App\Http\Form;

class ActivityDto
{

    /** @var \DateTime|null */
    private $date;

    /**
     * Get the value of date
     */
    public function getDate()
    {
        return $this->date;
    }

    /**
     * Set the value of date
     *
     * @return  self
     */
    public function setDate($date)
    {
        $this->date = $date;

        return $this;
    }
}
